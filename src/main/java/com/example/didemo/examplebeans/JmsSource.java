package com.example.didemo.examplebeans;

public class JmsSource {
    private String juser, jpassword, jurl;

    public String getJuser() {
        return juser;
    }

    public void setJuser(String juser) {
        this.juser = juser;
    }

    public String getJpassword() {
        return jpassword;
    }

    public void setJpassword(String jpassword) {
        this.jpassword = jpassword;
    }

    public String getJurl() {
        return jurl;
    }

    public void setJurl(String jurl) {
        this.jurl = jurl;
    }

    
}
